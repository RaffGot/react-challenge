import { BrowserRouter, Route, Routes } from "react-router-dom";

const RouterMain = ({ routes }) => {
  return (
    <BrowserRouter>
      <Routes>
        {routes.map((route) => (
          <Route key={route.path} path={route.path} element={route.element} />
        ))}
      </Routes>
    </BrowserRouter>
  );
};

export default RouterMain;
